export function detectDomain(text: string): Domain | null {
  const lower = text.toLowerCase();
  // Score each domain by how many keywords match
  const scores = (Object.entries(DOMAIN_KEYWORDS) as [Domain, string[]][])
    .filter(([d]) => d !== "other")
    .map(([domain, keywords]) => ({
      domain,
      score: keywords.filter((kw) => lower.includes(kw)).length,
    }))
    .filter(({ score }) => score > 0)
    .sort((a, b) => b.score - a.score);

  return scores[0]?.domain ?? null;
}
