export function detectCapabilities(text: string): AICapability[] {
  const lower = text.toLowerCase();
  return (Object.entries(CAPABILITY_KEYWORDS) as [AICapability, string[]][])
    .filter(
      ([cap, keywords]) =>
        cap !== "other" && keywords.some((kw) => lower.includes(kw)),
    )
    .map(([cap]) => cap);
}

